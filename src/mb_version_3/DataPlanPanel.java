    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
      String cost = Cost.getText();
      Object gb = GB.getSelectedItem();
      String operator = Operator.getText();
      String secondLine = SecondLine.getText();
      String textMess = TextMess.getText();
      
      if(cost.isEmpty() || gb.equals("Select") || operator.isEmpty() || secondLine.isEmpty() || textMess.isEmpty()){
          JOptionPane.showMessageDialog(null, "Error de ingreso:\n"
                    + "Por favor ingrese todos los datos solicitados", "Informaci√≥n incompleta",
                    JOptionPane.ERROR_MESSAGE);
      }else{
          JOptionPane.showMessageDialog(null, "Se ha a√±adido correctamente el Plan de Celular",   "Correcto",
                    JOptionPane.INFORMATION_MESSAGE);
                    DataPlanPanel.setVisible(false);
                    DataPlanPanel.removeAll();
                   // DataPlanPanel.add(Cost);
                    DataPlanPanel.setVisible(true);
                    
        BufferedWriter bw = null;
        FileWriter fw = null;

        try {
            String data = GB.getSelectedItem() + "," + SecondLine.getText() + "," + TextMess.getText()
                    + "," + Cost.getText() + "," + Operator.getText();
            File file = new File("C:/Users/Brian/Documents/planes.txt");
            if (!file.exists()) {
                file.createNewFile();
            }
            fw = new FileWriter(file.getAbsoluteFile(), true);
            bw = new BufferedWriter(fw);
            bw.write(data);
            bw.newLine();
            System.out.println("informaci√≥n agregada!");
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                            //Cierra instancias de FileWriter y BufferedWriter
                if (bw != null)
                    bw.close();
                if (fw != null)
                    fw.close();
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }
        
    }//GEN-LAST:event_AceptarActionPerformed

